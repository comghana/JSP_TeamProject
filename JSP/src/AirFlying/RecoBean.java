package AirFlying;

public class RecoBean {

    private String reco_name;
    private String reco_file;
    private String reco_status;
    
	public String getReco_name() {
		return reco_name;
	}
	public void setReco_name(String reco_name) {
		this.reco_name = reco_name;
	}
	public String getReco_file() {
		return reco_file;
	}
	public void setReco_file(String reco_file) {
		this.reco_file = reco_file;
	}
	public String getReco_status() {
		return reco_status;
	}
	public void setReco_status(String reco_status) {
		this.reco_status = reco_status;
	}
}

