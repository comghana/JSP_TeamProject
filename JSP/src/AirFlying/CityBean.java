package AirFlying;

/*
 * 작성자: 장지은
 * 파일 이름: CityBean.java
 * 목적: 추천 도시 관련 Bean파일
 */

public class CityBean {
	private String city_name;
	private String city_file;
	private String city_io;
	public String getCity_name() {
		return city_name;
	}
	public void setCity_name(String city_name) {
		this.city_name = city_name;
	}
	public String getCity_file() {
		return city_file;
	}
	public void setCity_file(String city_file) {
		this.city_file = city_file;
	}
	public String getCity_io() {
		return city_io;
	}
	public void setCity_io(String city_io) {
		this.city_io = city_io;
	}
	
}
